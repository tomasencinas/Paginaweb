<h2>Comida de la Calle (Comida Rápida):</h2>
<li>Humitas: es un plato de origen peruano, que se extendió por la Cordillera de los Andes (Bolivia, Chile, Argentina, Chile, Ecuador). Consiste principalmente en una pasta de maíz, envuelta y cocida en las hojas de una mazorca de maíz. Puede acompañarse con huevos, aguacate, cebollas, y distintas especies para darle mas sabor. Las humitas se han convertido en un plato muy conocido, pueden ser de sal y de dulce.   
</li>
<br>
<li>Bolones: Los bolones de verde se preparan con plátanos verdes o plátanos machos, los cuales se cocinan y se aplastan hasta formar una especie de masa, la que se rellena con queso o chicharrón o chorizo. En la costa no hay desayuno más rico que un delicioso bolón de verde acompañado por un huevo frito, aji, y una taza de cafecito. 
</li><br>
<li>Empanadas: es una fina masa de pan, rellena de preparación salada o dulce. Puede ser cocida al horno o frita. La masa generalmente, es de harina de trigo, harina de maíz, u otros cereales y normalmente lleva algún tipo de manteca o aceite. El relleno puede incluir distintas variedades de carnes, verduras, queso, frutas entre otros. Existen distintos tipos: empanada de morocho: hecha de maíz con arroz. Empanada de verde: hecho de plátano verde con queso, empanada de viento: hecha de harina con queso, y un poco de azúcar.</li>
<br>
<li>Cevichochos: es un plato vegetariano preparado a partir de semillas de chocho, combinado con limón, sal, cebolla, salsa de tomate, maíz tostado y banano frito.</li>
<br>
<li>Choclo: también conocido como el maíz Peruano. Es un tipo de maíz con un sabor distinto al que estamos acostumbrados. En muchos casos, es vendido como comida de calle, junto con queso y carne desmechada.
</li><br>
<li> Pan de Yuca: es un tipo de pan elaborado con queso blanco, almidón de yuca, huevos, polvo de hornear; que se amasa y e hornea. Típico de la region de la costa del Ecuador, así como de la parte sur de Colombia.
</li><br>
<li>Patacones: frito a base de plátanos verdes, popular en varios países de latinoamerica.
</li><br>
