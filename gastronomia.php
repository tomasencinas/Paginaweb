<h2>Comida Ecuatoriana</h2>
La gastronomía del Ecuador, se caracteriza por su variada forma de preparar comidas y bebidas. Dentro del país podemos encontrar 4 principales regiones: costa, sierra, oriente y región insular (Galápagos) con sus respectivas costumbres y tradiciones. <br> Dentro de la ¨comida típica¨ podemos encontrar; arroz, huevo, papas, aguacate, carne de res o de cerdo. <br>  Debido a que el Ecuador consta con una amplia línea costera, los productos marinos son abundantes. Entre la comida Ecuatoriana se encuentran platos como, el ceviche, el bollo, la fanesca, el sancocho de pescado etc.<br>Por otro lado, la zona tropical del Ecuador es muy rica en sus distintas variedades de frutas: naranjillas, guanabana, granadilla, pitahaya etc. Todos estos productos son distribuidos entre las distintas regiones, es por eso que los mejores platos se encuentran alrededor de todo el Ecuador.

