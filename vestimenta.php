<h3>VESTIMENTA TRADICIONAL ECUATORIANA PARA LAS MUJERES</h3>
<br>
<p>
En los trajes típicos de las mujeres si cabe hay más variedad que en los masculinos. Las blusas blancas bordadas, las faldas con vuelo de colores vivos como el rosa fucsia, el rojo o el verde son muy característicos.
<br>
El traje típico de natabuela con falda azul, camisa blanca floreada un gran sombrero y un chal sobre la cabeza de colores es una estampa típica de los trajes. Los trajes típicos también tienen su versión de gala con faldas y fajines de ricas telas bordados como el “Follón de cola de Chola Cuencana”.
<img src="images/vestimenta-mujer.jpeg" alt=""><br>
Las mujeres llevan un montón de collares hechos de cuentas generalmente dorados que se llaman Hualcas, también los llevan en las manos.
También se pueden vestir con lo que se conoce como Anaco, y que es una especie de de manta con la que se  envuelven a modo de falda hasta los talones, generalmente de color azul y una faja bordada que la sostiene a la altura de la cintura.
Como calzado es típico llevar zapatillas hechas de tela y fibra de totora que se llaman alpargatas.
<br>
<h3>EL PUEBLO ECUATORIANO Y SUS FORMAS DE VESTIR</h3>
<br>
El vestido es  una seña de identidad cultural en muchas zonas del mundo. Ecuador tiene a gala su gran riqueza cultura que queda reflejada en la forma de vestir. En Ecuador la gente puede identificar de que parte es la persona según su forma tradicional de vestir.  Colorido, rojo, rosa,s verdes, así como el blanco el azul o el negro, Rayas, sombreros y ponchos son característicos en los trajes típicos del Ecuador. Los trajes tienen su versión masculina y la femenina.
Entre los trajes típicos del Ecuador están: el chibuleos, los salasacas, los de chola cuencana, los pilahuin, los shuar, los de cañar, los de cayanbeño, los de cofan, Machachi, los de natabuela,traje típico de Otovalos, traje de Santo Domingo de los colorados.
Como ya mencioné anteriormente, quizás el traje típico Ecuatoriano que más se conoce es el de los Otovalos, y que se distingue por ser de un color claro para la camisa y el pantalón y con un ponchocubriendo (en el caso de los hombre) y de una blusa blanca y falda de colores (en el caso de la mujer).
</p>
