    <h3>Vestimenta Típica</h3>
<h4>En este país la vestimenta es variada y vistosa. Los modelos de vestidos cambian de una provincia a otra, pero siempre guardan rasgos generales, son sus colores y adornos los que los hacen ver más vistosos.
<br>
Ecuador es un crisol de culturas indígenas que todavía conservan casi todas sus tradiciones intactas, los trajes típicos son una de estas tradiciones y de estos podemos avanzaros que no existen tan sólo uno, sino que podemos encontrarlos pertenecientes a las que han sido, y son, civilizaciones del país. Quizás el más conocido o popular sea el llamado Otavalo.
Desde hace miles de años Ecuador  estaba poblado por distintas civilizaciones, muchas de estas culturas siguen existiendo hoy y forman parte de la riqueza multicultural del Ecuador. Entre las culturas indígenas destacan los Achuar, los Awa, los Cranqui, los Cayambis, los Cañaris, los Cayapas,  los Salascas, los pilahuin, los Caras, los Huancavilcas, los Imbaburras, los Quechuas de los Andes, los Otavalos.</h4>


<h3>VESTIMENTA TRADICIONAL ECUATORIANA PARA HOMBRES</h3>
<p>
A pesar de que en cada zona hay un traje o pieza de ropa que puede ser característica, un look tradicional y típico del hombre de Ecuador es similar al traje de Otovano con sombrero de fieltro ropa blanca y poncho azul o un poncho que sea de colores llamativos (esta prenda les protege del frío y además es muy cómoda de llevar).
Por otro lado, La Shimba es tradicional en los hombres, es una trenza muy larga que llega hasta casi la cintura.
En las zonas de costa donde la temperatura es alta se usan las Guayaberas, una especie de camisa suelta que se usa en lugar de chaqueta.
<img src="images/vestimenta-hombre.jpg" alt="">
</p>
