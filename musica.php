<h1>Musicas de Ecuador</h1>
<div class="musica">

<h3>Ecuador es uno de los países que más se destacan dentro de la población latina de América y al igual que tantos otros, cuenta con formas de expresión artística que le han permitido crearse una identidad propia y entretener a todos sus habitantes.</h2>
<br>
<h4>Definición de música ecuatoriana</h4>
<br>
<p>
Se le conoce como música o ritmos ecuatorianos, a todos aquellos que surgen dentro de la República del Ecuador, como una manifestación cultural por medio del sonido. De aquí que hayan surgido algunos géneros que se consideran como exclusivos de esta parte del mundo; tales como «El Pasillo», el «Yaraví», el «Fandango» o «El Pasacalle».
<br>
Todos ellos hacen uso de instrumentos característicos de las regiones ecuatorianas y así mismo, se han vuelto una melodía muy popular en fiestas tradicionales y hasta cotidianas de la gente.
<br>
En la actualidad sin embargo, Ecuador también ha recibido la influencia de géneros extranjeros; por lo que no es de extrañar que entre su música también podamos encontrar ritmos actuales como el pop, el rock, la electrónica y un sinfín de canciones, que sin perder el sabor de los ritmos latinos, se fusionan con más de uno de ellos, para crear composiciones modernas y que gustan a públicos de distintas edades.
</p>
<br>
<h4>Géneros musicales ecuatorianos</h2>
<br>
<p>
Como ya hemos hecho mención antes, la música ecuatoriana resalta por la presencia de diferentes géneros que han enriquecido su amplio repertorio. Esto es algo que a menudo pasa con la expresión musical de varios países. Dentro de los géneros más famosos para Ecuador, es fácil identificar los que se leen a continuación:
<br>
    <strong>El Pasillo</strong><br>Este estilo folclórico surge tanto en Ecuador como en Colombia y además de haberse constituido como un género de música propio de dichas regiones, también cuenta con su propia danza tradicional. Tanto es así que incluso se considera una especie de símbolo, muy representativo dentro de ambos países. En Ecuador, «El Pasillo» surge de la mano de un par de diplomáticos procedentes de Colombia, en el año de 1877 en Quito. Recibe influencias directos del yarabí y el sanjuanito. Suele estar acompañado por letras que hablan acerca de temas melancólicos, como la pérdida del amor o la añoranza. Se puede decir que también ha pasado a ser considerada como «música nacional».
<br>
<strong>El Saltillo</strong><br>
 Proviene de la música tradicional andina y además de hacerse presente en Ecuador, también es escuchada en ciertas partes de Perú y Colombia. Fue cuando recién comenzaba el siglo pasado, que este género empezó a acrecentar su popularidad. A esto contribuyo el hecho de que, a diferencia de «El Pasillo», predominara en él un ritmo alegre que pronto lo convertiría en el favorito para festividades tradicionales, tanto de indígenas como de mestizos. Sus orígenes prehispánicos no quitan que incluso hoy, pueda escucharse en distintos puntos de reunión públicos. «El Sanjuanito» es tan característico para los ecuatorianos, que también dispone de su propia danza folclórica.
<br>
<strong>El Pasacalle</strong><br>
Se le conoce como género mestizo, debido a las raíces españolas con las que cuenta. A pesar de que inicialmente siempre fue interpretada por músicos ambulantes, hay que destacar que gozó de bastante fama durante el período del Barroco, durante el cual llegó a incluirse en repertorios de música considerada como culta. «La Follia Di Spagna» por ejemplo, fua una canción perteneciente a este estilo que adquirió tanta importancia a lo largo de Europa, que no faltaron compositores renombrados que pronto se inspirarán en ella para crear nuevas melodías clásicas. «El Pasacalle» tiene ritmos muy festivos y además de oírse en Ecuador, ha llegado a hacer acto de presencia en Argentina.
</p>
